    private void lblLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblLoginMouseClicked
                try {
            String user = txtUsuario.getText();
            String password = String.valueOf(txtPassword.getPassword());
            String query = "SELECT * FROM usuarios WHERE user='" + user + "' and password='" + password + "'";
            Statement st = cx.Conectar().createStatement();
            ResultSet rs = st.executeQuery(query);
            if (user.equals("") || password.equals("") || user.equals("Usuario") || password.equals("******")) {
                JOptionPane.showMessageDialog(this, "Alguno de los campos esta en blanco");
            } else {
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Bienvenid@ " + txtUsuario.getText() + " !!");
                    MenuPrincipal mnp =new MenuPrincipal();
                    this.setVisible(false);
                    mnp.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "El usuario no existe en la base de datos");
                    txtUsuario.setText("Usuario");
                    txtUsuario.setForeground(new Color(190, 193, 198));
                    txtPassword.setText("******");
                    txtPassword.setForeground(new Color(190, 193, 198));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(MenuInicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblLoginMouseClicked

